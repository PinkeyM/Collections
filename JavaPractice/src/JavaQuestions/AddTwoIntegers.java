package JavaQuestions;



public class AddTwoIntegers {

	public static void main(String[] args) {
	
		
		int i=10;
		int j=20;
		int c=i+j;
		System.out.println(c);
		
		System.out.println(i+j);
	
		//swapping of numbers using temp:
	int temp;
	temp=i;
	i=j;
	j=temp;
	System.out.println(i);
	System.out.println(j);
	
		
		
		
	}

}
